//var declaração de variavel universal
var c = 0;

//let declara variável em escopo local
let a = 10;

//const declara constantes
const b = "10";

console.log("a:"+ a + " " + "b:" + '"10"');
// == Compara valores ignorando o tipo
console.log("==" + " " + (a == b));

// === Compara valores levando em consideração o tipo
console.log("===" + " " + (a === b));

// != diferença ignorando tipo
console.log("!=" + " " + (a != b));

// !== diferença levando em consideração o tipo
console.log("!==" + " " + (a !== b));

//typeof retorna o tipo da variavel
console.log("a: " + typeof a);

